package com.tcv;

public class ExamplesIf2 {

    public static void main(String[] arg) {

        // Define a variable
        int number = 8;

        // Check if the number is even or odd
        if (number % 2 == 0) {
            System.out.println("The number " + number + " is even.");
        } else {
            System.out.println("The number " + number + " is odd.");
        }
    }
}

